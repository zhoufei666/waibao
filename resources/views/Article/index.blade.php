            <h2 class="title"><a href="">泪湿眼窝的一瞬</a></h2>

            <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">我毫不隐晦。</span></p>
                    <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">刚刚从七斋二层的宣传部下楼去办个小事儿，在我走出门口的时候，前面刚好有一对情侣走过，那男孩停下来，轻轻地吻了女孩一下。我的眼光扫过，心中一颤。等到一分钟后我回来，走进七斋一层大厅的玻璃门，瞬间泪湿眼窝。</span></p>
                    <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">我不知道是什么触动了我。</span></p>
                    <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">我的<a href="https://lvwenhan.com/tag/%E7%94%9F%E6%B4%BB">生活</a>从我眼前划过，我的情感从我眼前划过，我对异性的不相信从我眼前划过，我目前的精神状态从我眼前划过。我到现在还是不知道到底是什么触动了我。</span></p>
                    <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">我已经小心翼翼到了如此地步。如此这般的猜疑，悲观，失落，自怨自艾。我似乎早已忘了快乐这个生活的本质目的，与人斗，与己斗，与世界斗，与生活斗。</span></p>
                    <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">我至今清楚地记得我自我反省过那个“我把一切视为竞争”的错误言论。用一句错误的话来描述<a href="https://lvwenhan.com/tag/%E8%87%AA%E5%B7%B1">自己</a>，最终自己就会变成那个错误，这是很浅显的自我催眠的道理。</span></p>
                    <p class="MsoNormal" style="text-align:left;text-indent:24.0pt;" align="left"><span style="font-size:12.0pt;font-family:&quot;微软雅黑&quot;,&quot;sans-serif&quot;">从刚刚事情发生，到我写到现在，泪水一直在眼窝里打转。也许，我应该相信些什么。</span></p>
